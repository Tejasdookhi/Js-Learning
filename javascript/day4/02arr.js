let arr = [1, 2, 3, 4]//1,2,3,4 i want to add 5 and 6

// push method adds element at last
arr.push(5,6);
console.log(arr);//1,2,3,4,5,6

//pop method removes element from last
arr.pop();
console.log(arr);//1,2,3,4,5


//shift method removes first elemnt of an array
arr.shift();//2,3,4,5
console.log(arr);

//unshift add element at start
arr.unshift(1,10);
console.log(arr);//1,10,2,3,4,5





